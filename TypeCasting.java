public class TypeCasting {
    public static void main(String ar[]) {
        byte b=40;
        short s=60;
        char c='A';
        int i=5640;
        float f=34.556f;
        double d=5.646465;
        double result=(f*b)+(i/c)-(d*s);
        System.out.println((f*b)+" "+(i/c)+" "+(d*s));
        System.out.println(result);

    }
}
